package com.vtb.java.lesson7.homework.task3;

public class MyEntry {
    private Integer key;
    private Integer value;

    public MyEntry(Integer key, Integer value) {
        this.key = key;
        this.value = value;
    }

    public Integer getKey() {
        return key;
    }

    public Integer getValue() {
        return value;
    }
}
